# Develop a program that reads a student's two grades, calculates and display its average.

grade1 = float( input(' type grade 1: ') )
grade2 = float( input(' type grade 2: ') )

average = ( grade1 + grade2 ) / 2

print(' average: {}'.format(average) )


# OUTPUT -->
# type grade 1: 10
# type grade 2: 8
# average: 9.0
